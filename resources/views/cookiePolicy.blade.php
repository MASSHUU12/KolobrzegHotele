    <h1>Polityka plików "cookies".</h1>
    <p>Niniejsza Polityka dotyczy plików "cookies" i odnosi się do wszystkich podstron tego serwisu.</p>
    <h1>Czym są pliki "cookies"?</h1>
    <p>Poprzez pliki "cookies" należy rozumieć pliki tekstowe, przechowywane w urządzeniach użytkowników (takich jak komputer, telefon lub tablet), przesyłane przez strony internetowe. Pliki te pozwalają rozpoznać urządzenie użytkownika i odpowiednio wyświetlić stronę internetową dostosowaną do jego indywidualnych preferencji, umożliwiają wyświetlenie strony internetowej w języku użytkownika "zapamiętanym" przez te pliki, a także wykorzystanie innych ustawień strony internetowej wybranych przez użytkownika. "Cookies" zazwyczaj zawierają nazwę strony internetowej z której pochodzą, czas przechowywania ich na urządzeniu końcowym oraz unikalny numer.
    </p>
    <h1>W jaki sposób wykorzystujemy pliki "cookies"?</h1>
    <p>Pliki "cookies" używane są przez nas do przechowywania informacji o tym jakie oferty użytkownik zapisał jako "ulubione" (oraz do późniejszego ich wyświetlenia), te informacje są przechowywane lokalnie na komputerze użytkownika i nie wykorzystujemy ich w żaden inny sposób, mają służyć tylko i wyłącznie wygodzie użytkownika.</p>
    <h1>Jakich plików "cookies" używamy?</h1>
    <p>Stosowane są przez nas pliki "stałe", pozostają one na urządzeniu użytkownika przez czas określony w parametrach plików "cookies" lub do momentu ich ręcznego usunięcia przez użytkownika.</p>
    <h1>Czy pliki "cookies" zawierają dane osobowe?</h1>
    <p>Nie, żadne informacje przechowywane w tych plikach nie są danymi osobowymi ani nie pozwalają na zidentyfikowanie użytkownika. Znajdują się w nich tylko i wyłącznie informacje pozwalające na  wyświetlenie zapisanych ofert.</p>
